<p>El banc de temps és un sistema d'intercanvi de serveis per temps.</p>
<p>La unitat d'intercanvi no són els diners habituals, sinó una mesura de temps, en el nostre cas el treball per hores.</p>
<p>És un sistema d'intercanvi de serveis per serveis. Té l'avantatge de fomentar les relacions socials i la igualtat entre diferents estrats econòmics.</p>
<p>El temps de cada participant es valora de forma igualitària, sigui un principiant o un expert, un enginyer o un paleta.</p>
<p>El banc de temps és, a més, una eina cabdal per revitalitzar la vida de la comunitat.</p>
<p>Esperem que serveixi per unir més les persones!</p>

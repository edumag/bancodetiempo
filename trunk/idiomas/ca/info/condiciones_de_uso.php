<p><strong>Limitacions</strong></p>
<p>Els serveis no queden garantits. El proveïdor d'un servei pot no satisfer les expectatives de qui el rep. La valoració de l'esforç que hi posa cadascú és el que fa que el banc de temps funcioni.</p>

<p><strong>Confidencialitat i privacitat</strong></p>
<p>El banc de temps es basa en un criteri de confiança mútua. Els usuaris han de vetllar per mantenir la privacitat i la confidencialitat de la resta. Un usuari pot ser expulsat de la comunitat si no respecta aquesta premissa. L'única excepció per compartir aquest tipus d'informació es produeix quan un usuari creu que la salut, integritat o seguretat d'un altre (o altres) estan en perill.</p>

